    private void jFormattedTextField1FocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jFormattedTextField1FocusGained
        // TODO add your handling code here:
        String agencia = jTextField1.getText();
        String conta = jTextField2.getText();

        ContaController trs = new ContaController();
        if (!agencia.trim().equals("") && !conta.trim().equals("")) {
            if (ClienteController.clienteExiste(jTextField1, jTextField2) > 0) {

                if (Funcoes.getId() == Integer.valueOf(conta.trim())) {
                    jLabel7.setText("<html><head></head><body><div>Você não pode transferir de sua conta para sua conta.</div></body></html>");
                    jLabel7.setForeground(Color.red);
                    jButton1.setEnabled(false);
                } else {
                    jLabel7.setText(trs.getNomeCliente(Integer.parseInt(agencia.trim()), conta.trim()));
                    Color r = new Color(0, 102, 0);
                    jLabel7.setForeground(r);
                    jButton1.setEnabled(true);
                }
            } else {
                jLabel7.setText("<html><head></head><body>Conta não encontrada.</body></html>");
                jLabel7.setForeground(Color.red);
                jButton1.setEnabled(false);

            }
        }
    }//GEN-LAST:event_jFormattedTextField1FocusGained

