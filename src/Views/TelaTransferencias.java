    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ContaController tr = new ContaController();

        //convert string numeric in double
        Number valor;
        try {
            valor = NumberFormat.getInstance().parse(jFormattedTextField1.getText().trim());
            /*Transforma o 15,5 em 15.5*/

            //A agência de quem receberá a transferência.
            String agencia = jTextField1.getText().trim();
            //Número da ContaController de quem receberá a transferência.
            String conta = jTextField2.getText().trim();

            try {

                if (agencia.equals("") || conta.equals("") || valor.equals("")) {
                    JOptionPane.showMessageDialog(null, "Todos os campos são obrigatórios.",
                            "Atenção!", JOptionPane.ERROR_MESSAGE);
                    jFormattedTextField1.requestFocus();
                } else {

                    int reply = JOptionPane.showConfirmDialog(
                            null,
                            "Deseja fazer essa transferência?",
                            "Atenção",
                            JOptionPane.YES_NO_OPTION);

                    if (reply == JOptionPane.YES_OPTION) {

                        ContaController c = new ContaController();

                        BigDecimal f1 = c.consultarSaldo(Integer.parseInt(String.valueOf(Funcoes.getId())));

                        BigDecimal f2 = Dinheiro.parseStringToDouble(valor.toString());

                        int retval = f1.compareTo(f2);

                        if (retval == 1) {
                            JOptionPane.showMessageDialog(null, "A Operação NÃO pôde ser Realizada, "
                                    + "porque o valor da transferência é maior que o valor do saldo!",
                                    "Atenção!", JOptionPane.ERROR_MESSAGE);
                        } else if (retval == -1 || retval == 0) {

                            //Saída da conta de quem está tranferindo.
                            if (tr.efetuarTransferencia(Funcoes.getId(), Dinheiro.parseStringToBiDecimal(valor.toString()), Funcoes.getDataFormat(),
                                    ContaController.TRANSF_SAIDA, contaTpp, Integer.parseInt(conta), "Tranferência para outra conta.") > 0) {
                                //Entrada na outra conta
                                tr.efetuarTransferencia(Integer.parseInt(conta), Dinheiro.parseStringToBiDecimal(valor.toString()),
                                        Funcoes.getDataFormat(), ContaController.TRANSF_ENTRADA, contaTpp, Funcoes.getId(), jTextArea1.getText());
                                JOptionPane.showMessageDialog(null, "Operação realizada com sucesso!",
                                        "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                                this.setVisible(false);
                            } else {
                                jLabel7.setText("<html><head></head><body>Não é um número válido.</body></html>");
                            }

                        }
                    }

                }

            } catch (SQLException ex) {
                Logger.getLogger(TelaTransferencias.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (ParseException ex) {
            Logger.getLogger(TelaTransferencias.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

